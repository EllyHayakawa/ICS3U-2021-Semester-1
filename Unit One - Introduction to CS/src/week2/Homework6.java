package week2;

/**
 * calculate the volume of a sphere
 */
public class Homework6 {
    public static void main(String[] args) {
        final double pi = Math.PI;
        int r = 2;
        double v = (4/3) * pi * (r * r *r);
        System.out.println("The volume of the sphere is " + v);
    }
}
